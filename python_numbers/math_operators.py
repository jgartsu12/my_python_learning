print('Addition')
get_sum = 100 + 42 
print(get_sum) # prints 142, an integer  

print('Subtraction')
print(100 - 42.3) # prints 57.7;; integer and floats can work together

print('Division')
print(100 / 42)
print(100 / 38)

print('Floor Division')
print(100 // 42)
print(100 // 38)

print('Multiplication')
print(100 * 42)

print('Modulus')
print(100 % 42)

print('Exponents')
print(100 ** 42)