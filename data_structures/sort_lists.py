# How to Sort Lists [] in Python
tags = ['py', 'dvp', 'tutorials', 'code']

print(tags) # prints in exact order of their index what if u want to sort by their alphabetical value
# lists are muttable
# use sort() to alphabetize list so not in the index order
tags.sort()

print(tags) # printed ['code', 'dvp', 'py', 'tutorial'] -- list sort() default in alphabetic order