# Guide to the sorted Function in Python
# sort list and not change original list
sale_prices = [
    100,
    83,
    220,
    40,
    100,
    400,
    10,
    1,
    3
]

sale_prices.sort() # this changesd the entire structure of the items in the list

print(sale_prices) # [1, 3, 10, 40, 83, 100, 220, 400]